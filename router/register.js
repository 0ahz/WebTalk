var url = require('url');

/**
 +------------------------------------------------------------------------------
 * 输出注册页面，GET请求
 +------------------------------------------------------------------------------
 */
exports.index = function(req, res){
	res.render('register', { 
		title:'Register',
		js_path:'register.js',
		css_path:'login_style.css'
	});
}

/**
 +------------------------------------------------------------------------------
 * 处理用户注册请求，POST请求
 +------------------------------------------------------------------------------
 */
exports.register = function(req, res){
	console.log('register.index');
	var path = url.parse(req.url).pathname;	
	res.render('index', { url:path });
}